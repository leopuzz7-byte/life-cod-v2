// KEYTO Путь к успеху (методика 2) — 9 этапов запуска проекта + персонализация по матрице.
// 9 этапов = 9 энергий (1-9). Наличие энергии берём из психоматрицы даты рождения.
import { computeMatrix } from "./keytoMatrix";

export interface SuccessStep { title: string; text: string; }
export interface SuccessStage {
  energy: number;
  title: string;
  steps: SuccessStep[];
  summary: string;
  strong: string;
  weak: string;
}

export interface SuccessResult {
  day: number;
  month: number;
  year: number;
  present: Record<number, boolean>;
}

export function calculateKeytoSuccess(day: number, month: number, year: number): SuccessResult {
  const m = computeMatrix(day, month, year);
  const present: Record<number, boolean> = {};
  for (let i = 1; i <= 9; i++) present[i] = (m.counts[i] || 0) > 0;
  return { day, month, year, present };
}

