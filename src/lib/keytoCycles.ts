// KEYTO (методика 2) — циклы развития энергии.
// Ума: энергия стоит между соседними (цикл +1): вниз к N-1, вверх к N+1.
// Действия: триада +3: N, N+3, N+6 (сведённые). Тексты по смыслу книги, без длинных тире.

function reduce(n: number): number {
  while (n > 9) n = String(n).split('').reduce((a, b) => a + parseInt(b), 0);
  return n;
}
const wrap = (n: number) => ((n - 1 + 9) % 9) + 1; // держим в 1-9

