export interface UmaCycle {
  minusEnergy: number; plusEnergy: number; minus: string; plus: string;
}
export function umaCycle(v: number): UmaCycle {
  const minusEnergy = wrap(v - 1);
  const plusEnergy = wrap(v + 1);
  return {
    minusEnergy, plusEnergy,
    minus: `При спаде энергия ${v} скатывается к тени ${minusEnergy}: включаются ${shadow[minusEnergy]}. В этом состоянии решения принимаются на эмоциях, и вы теряете свою настоящую силу.`,
    plus: `При росте энергия ${v} раскрывается через ${plusEnergy}: развиваются ${gift[plusEnergy]}. Это и есть ваш путь вверх, когда сила работает на созидание.`,
  };
}

export interface ActionCycle { triad: number[]; positive: string; negative: string; }
export function actionCycle(v: number): ActionCycle {
  const a = v, b = reduce(v + 3), c = reduce(v + 6);
  return {
    triad: [a, b, c],
    positive: `Ваша энергия действия проходит путь ${a} - ${b} - ${c}: начинается через ${gift[a]}, переходит в ${gift[b]} и завершается через ${gift[c]}. Двигаясь по этому пути осознанно, вы получаете стабильный результат.`,
    negative: `В негативном проживании тот же путь превращается в ${shadow[a]}, затем ${shadow[b]}, и в итоге ${shadow[c]}. Это сигнал вернуться к осознанным действиям.`,
  };
}
